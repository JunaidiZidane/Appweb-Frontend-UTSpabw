                    <h4>3 Bulan hidup bersama Toyota Supra : Mengunjungi hantu masa depan Natal</h4>
                    <img src="../Asset/image/Review/supra.jpg" alt="100%" style="width: 100%;">
                    <p class="p mb-2" style="color: gray; width: 76%;">Toyota Supra (CarMagazine.uk)</p> 
                    <div class="isikonten">
                        <p>AE86, 2JZ, S15, RB26, Sil80 ... jika ini terdengar seperti kode kesalahan internet. Anda mungkin tidak boleh <span>mengunjungi Garage-D </span>, dan jika melakukannya. Anda mungkin diminta untul pergi. Berbasis dibangunan pertanian yang diubah diluar Waitfor, Garage-D dijalankan oleh Julian Smith dan berspealisasi dalam persiapan drift dan trek, terutama mobil Jepang dengan penggerak belakang yang terkait dengan kode alfanumerik tersebut, meskipun dia juga pria BMW.</p>
                        <p>Saya telah mengenal Julian selama lebih dari 15 tahun, di mana saat dia mengerjakan M3 saya, tidak ada yang kehilangan aksen Irlandia yang diwarnai Kiwi, dan - yang lebih terkenal - menyiapkan mobil untuk film Fast & Furious. Dia membangun reputasi sebagian pada bakatnya untuk berpikir di luar kotak seperti mekanik reli jadul - dia mungkin bisa membangun kembali motor Supra 2JZ dengan pengikat kabel dan kunci pas dari cracker Natal jika dia harus - tetapi Julian juga sangat ahli. pengemudi yang ulung.</p>
                        <p>Dia paling terkait dengan puncak kinerja tinggi Jepang tahun 90-an / awal-awal termasuk Mk4 Supras, S-body Nissans, dan R32-34 Skylines. Tapi baru-baru ini dia sedang menyelidiki opsi tuning untuk Mk5 Supra berbasis BMW.</p>
                    </div>
                    <ul class="awal">
                        <li><a href="#" style="color: blue;">Baca review lengkap Toyota Supra</a></li>
                    </ul>
                    <img src="../Asset/image/Review/supra1.jpg" alt="100%" style="width: 100%;">
                    <p class="p mb-2" style="color: gray; width: 76%;">Toyota Supra (CarMagazine.uk)</p>
                    <div class="isikonten">
                        <p>Dia bertanya apakah saya ingin menurunkan CAR Supra untuk dibandingkan dengan contoh tuned milik pelanggan Duncan Lang, mobil kuning di gambar-gambar ini. Pemilik Mk4 Supra umumnya tiba dengan kaus Gold's Gym melakukan pemadaman bergilir, tetapi Mk5 jelas menarik kerumunan yang berbeda jika Duncan menjadi perwakilan. Dia menikmati karir yang panjang bekerja dalam manajemen proyek untuk pembuat mobil dan pemasok utama, termasuk memastikan model Renault Sport dan Ford RS / ST dilengkapi dengan kursi.</p>
                        <p>Duncan berada di Garage-D dalam beberapa minggu setelah mendapatkan Toyota barunya. "Saya suka Supra, tapi rasanya agak tercekik, seolah-olah ada pembatas di knalpotnya, dan mungkin tidak semenarik yang saya harapkan," dia menyimpulkan.</p>
                        <p>Langkah pertama adalah merawat mesin 3.0 liter straight-six turbo ke peta ulang ECU tahap 1. Garis dasar stok tercatat pada 355bhp pada apa yang digambarkan sebagai jalan bergulir independen dan konservatif. Pemetaan ulang tidak langsung langsung, karena ternyata perangkat lunak pihak ketiga telah dibuat untuk mobil AS, tetapi dengan beberapa penyetelan yang bagus akhirnya mencatat 402bhp - lebih dari peningkatan 377bhp yang diumumkan untuk Supras AS pada tahun 2020 (jika tanpa Piston kompresi rendah model AS dan manifold buang enam port).</p>
                        <p>Baru-baru ini Supra Duncan telah mencapai tahap 2 ECU. Induksi standar, tetapi ada knalpot HKS de-cat baru yang masih berfungsi dengan flap aktif di outlet knalpot sisi pengemudi. Masalah dengan dyno berarti sosok tenaga kuda yang andal belum tercatat, tetapi secara subyektif saya dapat memberi tahu Anda: ITU BANYAK.</p>
                        <p>Meskipun saya tertarik dengan peningkatan daya, peningkatan sasis konservatif pada mobil Duncan sebenarnya lebih menarik; seperti yang saya catat bulan lalu, Supra kami tampaknya memiliki daya tarik yang jauh lebih sedikit daripada mobil yang pertama kali saya kendarai, dan meluncur lebih awal dan sangat cepat.</p>
                        <p>Ternyata mobil AS memiliki pembaruan suspensi, tetapi ada bukti online tentang ketidakkonsistenan penyelarasan pada mobil spesifikasi Euro, dan Garage-D menemukan contoh Duncan kurang dari ideal. Mereka telah menyesuaikan toe dan camber untuk mengimbangi dan memberi ban belakang lebih banyak tambalan kontak saat menikung, tetapi mobil ini juga menggunakan pegas penurunan Tein, yang menjatuhkan mobil 25mm di depan dan 20mm di belakang. Menjalankan spacer roda kecil, hasilnya tidak kentara, tetapi memberikan Supra posisi yang lebih terarah.</p>
                    </div>
                    <img src="../Asset/image/Review/supra2.jpg" alt="100%" style="width: 100%;">
                    <p class="p mb-2" style="color: gray; width: 76%;">Toyota Supra (CarMagazine.uk)</p>
                    <div class="isikonten">
                        <p>Duncan dengan ramah menawariku jalan-jalan. Saya menyalakan mobilnya dan knalpot HKS meletus seperti kembang api. Ini tentu memberi Supra suara yang lebih kuat, tetapi semburan pada overrun membuatnya terdengar seperti Anda berperilaku buruk bahkan ketika sebenarnya Anda tidak melakukannya. Aku akan lulus</p>
                        <p>Saya suka peningkatan daya, meskipun - respons throttle terasa jauh lebih mendesak, dan ada lebih banyak semangat yang langsung muncul di bawah kaki kanan Anda saat Anda berakselerasi, serta ujung atas yang jauh lebih kuat dan lebih kaya, di mana mobil stok kehabisan isapan. Terasa jauh lebih kuat bahkan ketika Anda menggunakan stabil yang saya kira 450bhp.</p>
                        <p>Ini mengencangkannya selama berkendara, jadi saya tidak pernah merasa nyaman menyalakannya dan meninju dalam shift dengan throttle disematkan, tetapi tampaknya strategi perpindahan untuk mobil delapan kecepatan lebih agresif di Sport, dan menempatkan celah yang lebih besar antara Sport dan Kenyamanan.</p>
                        <p>Perbedaan pada sasis cukup terasa. Pegas penurun yang pas sering kali dapat merusak kualitas pengendaraan, tetapi pegas Tein ini mungkin sebenarnya telah memperbaikinya. Ya, ini terasa lebih kencang dalam beberapa hal, tetapi Supra Duncan tampaknya tidak mengalami pemeriksaan mendadak di bagian belakang pada rebound yang kadang-kadang terjadi pada mobil standar. Juga terasa lebih konsisten ketika saya mengukir putaran off-throttle, seolah-olah ujung depan dan belakang terhubung lebih baik dan cara kehilangan pegangan (dalam batas-batas kontrol stabilitas yang diaktifkan sepenuhnya, mengingat kondisinya) lebih progresif - meskipun semua penyesuaian penjajaran digabungkan dalam perubahan nuansa ini.</p>
                    </div>
                    <img src="../Asset/image/Review/supra3.jpg" alt="100%" style="width: 100%;">
                    <p class="p mb-2" style="color: gray; width: 76%;">Toyota Supra (CarMagazine.uk)</p>
                    <div class="isikonten">
                        <p>Jika saya harus memilih dari semua mod ini, saya akan bermain-main dengan sasis terlebih dahulu, dan menarik bahwa hanya dengan mengubah penjajaran dapat membuat dampak besar, tanpa benar-benar memodifikasi bagian standar sama sekali.</p>
                        <p>Mengetahui Garage-D, bagaimanapun, ini hanyalah langkah pertama dalam perjalanan yang suatu hari akan mengarah ke mobil balap full-house untuk menyaingi monster 800bhp Mk4 yang telah dia simpan. Apa pun yang dilakukan Julian, itu akan sangat berharga untuk diwaspadai.</p>
                    </div>
